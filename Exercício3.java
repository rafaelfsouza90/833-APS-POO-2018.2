/*
 * Faça um Loop determinado (For) que imprima uma contagem regressiva a partir do 20.
 */
package exercício3;
public class Exercício3 {
    
    public static void main(String[] args) {
        
        System.out.println("Contagem regressiga de 20 a 0.");
        int n=20;
        while(n>-1){
            System.out.println ("Número: "+n);
            n--;
        }
    }
} 